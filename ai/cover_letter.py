def _format_analysis_for_drafter(analysis: dict) -> str:
    """Render the structured analysis as a focused brief for the drafter."""
    if not analysis:
        return ""

    parts: list[str] = ["РАЗБОР ВАКАНСИИ (используй для целевого письма):"]

    def _kv(label: str, key: str) -> None:
        v = analysis.get(key)
        if v:
            parts.append(f"- {label}: {v}")

    _kv("Тип компании", "company_type")
    _kv("Домен", "company_domain")
    _kv("Уровень роли", "role_level")
    _kv("Тип роли", "role_type")
    _kv("AI-интенсивность", "ai_focus_intensity")
    _kv("Тон вакансии", "vacancy_tone")

    pains = analysis.get("main_pains") or []
    if pains:
        parts.append("- Главные боли компании:")
        for p in pains:
            parts.append(f"  * {p}")

    techs = analysis.get("tech_requirements") or []
    if techs:
        parts.append(f"- Требуемый стек: {', '.join(techs)}")

    overlap = analysis.get("stack_overlap") or []
    if overlap:
        parts.append(f"- ПЕРЕСЕЧЕНИЕ со стеком кандидата: {', '.join(overlap)}")

    gaps = analysis.get("what_candidate_lacks") or []
    if gaps:
        parts.append(f"- ЧЕГО НЕТ у кандидата (не заявляй ложно как опыт и "
                     f"не упоминай отсутствие — эти темы в письме просто "
                     f"опусти): {', '.join(gaps)}")

    instr = analysis.get("instructions_in_letter")
    if instr and instr != "null":
        parts.append(f"- ПРЯМАЯ ИНСТРУКЦИЯ В ВАКАНСИИ (выполни точно): {instr}")

    _kv("Лучший проект из портфолио", "best_fit_from_candidate")
    _kv("Ключевой техфакт для письма", "key_tech_fact")

    avoid = analysis.get("must_avoid_in_letter") or []
    if avoid:
        parts.append("- ИЗБЕГАЙ в письме:")
        for a in avoid:
            parts.append(f"  * {a}")

    _kv("Стратегия письма", "letter_strategy")
    _kv("Рекомендуемая длина", "recommended_length")

    return "\n".join(parts)


