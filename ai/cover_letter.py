def _load_candidate_context() -> str:
    """Load candidate-specific resume context from a local file.

    Priority:
      1. prompts/candidate.txt  (your real data, gitignored)
      2. prompts/candidate.example.txt  (committed template)
      3. empty string  (cover letters will be generic — you should fix this)
    """
